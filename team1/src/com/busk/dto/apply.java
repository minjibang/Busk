package com.busk.dto;

public class apply {
	private int apply_no;
	private String apply_email;
	private String apply_comment;
	private int hire_no;
	
	private String aname;
	private String name;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAname() {
		return aname;
	}
	public void setAname(String aname) {
		this.aname = aname;
	}
	public int getApply_no() {
		return apply_no;
	}
	public void setApply_no(int apply_no) {
		this.apply_no = apply_no;
	}
	public String getApply_email() {
		return apply_email;
	}
	public void setApply_email(String apply_email) {
		this.apply_email = apply_email;
	}
	public String getApply_comment() {
		return apply_comment;
	}
	public void setApply_comment(String apply_comment) {
		this.apply_comment = apply_comment;
	}
	public int getHire_no() {
		return hire_no;
	}
	public void setHire_no(int hire_no) {
		this.hire_no = hire_no;
	}
	
	
}
