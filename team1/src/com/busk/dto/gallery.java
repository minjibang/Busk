package com.busk.dto;

public class gallery {
	private int gallery_no;
	private String gallery_email;
	private String gallery_file;
	
	//개수
	private String gallery_count;
	
	
	public String getGallery_count() {
		return gallery_count;
	}
	public void setGallery_count(String gallery_count) {
		this.gallery_count = gallery_count;
	}
	public int getGallery_no() {
		return gallery_no;
	}
	public void setGallery_no(int gallery_no) {
		this.gallery_no = gallery_no;
	}
	public String getGallery_email() {
		return gallery_email;
	}
	public void setGallery_email(String gallery_email) {
		this.gallery_email = gallery_email;
	}
	public String getGallery_file() {
		return gallery_file;
	}
	public void setGallery_file(String gallery_file) {
		this.gallery_file = gallery_file;
	}
	
	
}
